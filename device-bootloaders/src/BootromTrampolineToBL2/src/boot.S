.section .text.init, "ax", @progbits
.global _boot

_boot:
    csrwi matp, 0       # disable machine VM
    csrwi satp, 0       # disable supervisor VM
    li    t0, 0x6000
    csrs  mstatus, t0   # set floating point status fs=11 (dirty)
    csrwi fcsr, 0       # clear floating point status
    #csrwi mip, 0       # clear all pending interrupts

    addi x1,  x0, 0
    # sp set later by BL2
    # gp set later by BL2
    addi x4,  x0, 0
    addi x5,  x0, 0
    addi x6,  x0, 0
    addi x7,  x0, 0
    addi x8,  x0, 0
    addi x9,  x0, 0
    addi x10, x0, 0
    addi x11, x0, 0
    addi x12, x0, 0
    addi x13, x0, 0
    addi x14, x0, 0
    addi x15, x0, 0
    addi x16, x0, 0
    addi x17, x0, 0
    addi x18, x0, 0
    addi x19, x0, 0
    addi x20, x0, 0
    addi x21, x0, 0
    addi x22, x0, 0
    addi x23, x0, 0
    addi x24, x0, 0
    addi x25, x0, 0
    addi x26, x0, 0
    addi x27, x0, 0
    addi x28, x0, 0
    addi x29, x0, 0
    addi x30, x0, 0
    addi x31, x0, 0

#ifdef __riscv_flen
    fcvt.s.w f0,  x0
    fcvt.s.w f1,  x0
    fcvt.s.w f2,  x0
    fcvt.s.w f3,  x0
    fcvt.s.w f4,  x0
    fcvt.s.w f5,  x0
    fcvt.s.w f6,  x0
    fcvt.s.w f7,  x0
    fcvt.s.w f8,  x0
    fcvt.s.w f9,  x0
    fcvt.s.w f10, x0
    fcvt.s.w f11, x0
    fcvt.s.w f12, x0
    fcvt.s.w f13, x0
    fcvt.s.w f14, x0
    fcvt.s.w f15, x0
    fcvt.s.w f16, x0
    fcvt.s.w f17, x0
    fcvt.s.w f18, x0
    fcvt.s.w f19, x0
    fcvt.s.w f20, x0
    fcvt.s.w f21, x0
    fcvt.s.w f22, x0
    fcvt.s.w f23, x0
    fcvt.s.w f24, x0
    fcvt.s.w f25, x0
    fcvt.s.w f26, x0
    fcvt.s.w f27, x0
    fcvt.s.w f28, x0
    fcvt.s.w f29, x0
    fcvt.s.w f30, x0
    fcvt.s.w f31, x0
#endif

    # Jump to BL2
    li ra, 0x40400000
    jr ra
